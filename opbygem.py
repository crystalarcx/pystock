def render_allocation_charts(categories, target_percentages, actual_percentages, differences):
    """Renders all allocation charts (bar and pie)."""
    col1, col2 = st.columns(2)
    with col1:
        fig_comparison = go.Figure()
        fig_comparison.add_trace(go.Bar(name='ÁõÆÊ®ôÈÖçÁΩÆ', x=categories, y=target_percentages, marker_color='rgba(52, 152, 219, 0.7)'))
        fig_comparison.add_trace(go.Bar(name='ÂØ¶ÈöõÈÖçÁΩÆ', x=categories, y=actual_percentages, marker_color='rgba(231, 76, 60, 0.7)'))
        fig_comparison.update_layout(title='ÁõÆÊ®ô vs ÂØ¶ÈöõÈÖçÁΩÆÊØîËºÉ', xaxis_title='Ë≥áÁî¢È°ûÂà•', yaxis_title='ÈÖçÁΩÆÊØî‰æã(%)', barmode='group', template="plotly_white")
        st.plotly_chart(fig_comparison, use_container_width=True)
    
    with col2:
        colors = ['green' if x >= 0 else 'red' for x in differences]
        fig_diff = go.Figure(data=[go.Bar(x=categories, y=differences, marker_color=colors, text=[f"{x:+.1f}%" for x in differences], textposition='auto')])
        fig_diff.update_layout(title='ÈÖçÁΩÆÂ∑ÆË∑ù (ÂØ¶Èöõ - ÁõÆÊ®ô)', xaxis_title='Ë≥áÁî¢È°ûÂà•', yaxis_title='Â∑ÆË∑ù(%)', template="plotly_white", yaxis=dict(zeroline=True, zerolinewidth=2, zerolinecolor='black'))
        st.plotly_chart(fig_diff, use_container_width=True)
    
    col3, col4 = st.columns(2)
    with col3:
        fig_target_pie = px.pie(values=target_percentages, names=categories, title='ÁõÆÊ®ôË≥áÁî¢ÈÖçÁΩÆ', color_discrete_sequence=px.colors.sequential.Blues_r)
        st.plotly_chart(fig_target_pie, use_container_width=True)
    with col4:
        fig_actual_pie = px.pie(values=actual_percentages, names=categories, title='ÂØ¶ÈöõË≥áÁî¢ÈÖçÁΩÆ', color_discrete_sequence=px.colors.sequential.Reds_r)
        st.plotly_chart(fig_actual_pie, use_container_width=True)

